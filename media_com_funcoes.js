function mediaAlunos(n1, n2, n3, n4){
    let media = (n1 + n2 + n3 + n4)/4
    return 'sua media é', media
}

// Calculando a média dos alunos

let notasPatrick = mediaAlunos(3, 5, 8, 4)
console.log("A média do Patrick é : ", notasPatrick)

let notasJurema = mediaAlunos(8, 7, 9, 3)
console.log("A média da Jurema é : ", notasJurema)